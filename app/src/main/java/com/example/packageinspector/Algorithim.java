package com.example.packageinspector;

public class Algorithim {
    private String algorithim;
    private String code;

    public Algorithim(String algorithim, String code) {
        this.algorithim = algorithim;
        this.code = code;
    }

    public String getAlgorithim() {
        return algorithim;
    }

    public String getCode() {
        return code;
    }
}
